abstract class MHRunnable3 implements Runnable {
    public MHRunnable3(Object o) {
        obj = o;
    }

    @Override
    public void run() {
        try{
            run(obj);
        }catch (IllegalStateException e) {
            Log.e("MediaHelper3", "run(obj);处异常");
            e.printStackTrace();
        }
    }

    public abstract void run(Object o);

    private Object obj;
}

