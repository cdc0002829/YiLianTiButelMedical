    private void syncPlay(int bits, int rate, int nchannels, String filepath,
                          int timeout) {

        resetSaveRec();
        tier_data = cpFile("taier_01.pcm", 48000);

        new Thread(new MHRunnable3(new RecordParam3(bits, rate, nchannels,
                filepath, timeout, 0)) {
            public void run(Object o) {
                RecordParam3 param = (RecordParam3) o;
                System.out.println("start play.");
                Log.w("sync", "start play");

                int frameLen = param.rate * 2 / 50; // 20ms 8192; //

                AudioTrack audioTrack = null;
                try {
                    int bufsize = android.media.AudioTrack.getMinBufferSize(
                            param.rate, AudioFormat.CHANNEL_OUT_MONO,
                            (param.bits == 16) ? AudioFormat.ENCODING_PCM_16BIT
                                    : AudioFormat.ENCODING_PCM_8BIT);

                    audioTrack = new AudioTrack(AudioManager.STREAM_VOICE_CALL,
                            param.rate, AudioFormat.CHANNEL_OUT_MONO,
                            (param.bits == 16) ? AudioFormat.ENCODING_PCM_16BIT
                                    : AudioFormat.ENCODING_PCM_8BIT, bufsize,
                            AudioTrack.MODE_STREAM);
                    audioTrack.play();

                    isPlaying = true;
                    int pre_voice_count = 5;
                    int null1_count = 40;
                    int pause1_count = 5;
                    int null2_count = 40;
                    int pause2_count = 5;
                    int pause_idx = 0;
                    int delay_count = 100;
                    int sleep_t = 20;
                    int null3_count = 15;
                    int tier_count = 1;
                    int null4_count = 5;

                    while (isPlaying) {
                        if (pre_voice_count > 0) {
                            audioTrack.write(vpause1khz_26db[pause_idx], 0,
                                    frameLen);
                            pause_idx++;
                            pre_voice_count--;
                        } else if (null1_count > 0) {
                            audioTrack.write(null_pcm, 0, frameLen);
                            null1_count--;
                            pause_idx = 0;
                        } else if (pause1_count > 0) {
                            audioTrack.write(vpause1khz_26db[pause_idx], 0,
                                    frameLen);
                            pause_idx++;
                            pause1_count--;
                        } else if (null2_count > 0) {
                            audioTrack.write(null_pcm, 0, frameLen);
                            null2_count--;
                            pause_idx = 0;
                        } else if (pause2_count > 0) {
                            if (pause2_count == 5) {
                                inst.setSaveRec();
                                sleep_t = 15;
                            }
                            //audioTrack.write(three, 0, three.length);
                            audioTrack.write(vpause1562_2812_3437hz_16db[pause_idx], 0,
                                    frameLen);
                            pause_idx++;
                            pause2_count--;
                        } else if (null3_count > 0) {
                            audioTrack.write(null_pcm, 0, frameLen);
                            null3_count--;
                        } else if (tier_count > 0) {
                            audioTrack.write(tier_data, 0, tier_data.length);
                            tier_count--;
                        } else if (null4_count > 0) {
                            audioTrack.write(null_pcm, 0, frameLen);
                            null4_count--;
                        } else if (delay_count > 0) {
                            delay_count--;
                        } else {
                            break;
                        }
                        Thread.sleep(sleep_t);
                    }
                } catch (Exception e) {

                } finally {
                    if (audioTrack != null) {
                        audioTrack.stop();
                        audioTrack.release();
                    }
                    try{
                        inst.stopRecord();
                    }catch(Exception e){
                        e.printStackTrace();
                        Log.e("MediaHelper3", "inst.stopRecord()异常");
                    }

                    System.out.println("play end.");
                    Log.w("sync", "play end.");
                }
            }
        }).start();

    }

    private void syncRecord(int bits, int rate, int nchannels, String filepath,
                            int timeout) {
        new Thread(new MHRunnable3(new RecordParam3(bits, rate, nchannels,
                filepath, timeout, 0)) {

            public void run(Object o) {
                RecordParam3 param = (RecordParam3) o;
                System.out.println("start record.");
                Log.w("sync", "start record.");
                int cnt = 0;
                int frameLen = param.rate * 2 / 50; // 20ms 8192; //

                AudioRecord audioRecord = null;
                DataOutputStream dos = null;

                try {
                    int bufferSize = AudioRecord.getMinBufferSize(param.rate,
                            AudioFormat.CHANNEL_IN_MONO,
                            (param.bits == 16) ? AudioFormat.ENCODING_PCM_16BIT
                                    : AudioFormat.ENCODING_PCM_8BIT);
                    System.out.println("record buffer size:" + bufferSize);

                    audioRecord = new AudioRecord(
                            MediaRecorder.AudioSource.MIC, param.rate,
                            AudioFormat.CHANNEL_IN_MONO,
                            (param.bits == 16) ? AudioFormat.ENCODING_PCM_16BIT
                                    : AudioFormat.ENCODING_PCM_8BIT, bufferSize);

                    dos = new DataOutputStream(new BufferedOutputStream(
                            new FileOutputStream(new File(param.filepath))));

                    System.out.println("begin record.");
                    byte[] buffer = new byte[frameLen];
                    audioRecord.startRecording();

                    isRecording = true;
                    inst.initSyncRec();
                    boolean isSaveFirst = true;

                    while (isRecording) {
                        audioRecord.read(buffer, 0, frameLen);
                        try{
                            inst.recAFrame();
                        }catch(Exception e){
                            e.printStackTrace();
                            CustomLog.e("MediaHepler3","声音检测异常");
                        }
                        if (inst.isSaveRec()) {
                            try {
                                {
                                    if (isSaveFirst) {

                                        int reclen = inst.recLen() * 16;
                                        if (reclen > 0){
                                            dos.write(buffer,
                                                    frameLen - reclen, reclen);
                                        }
                                        else{
                                            dos.write(buffer, 0, frameLen);
                                        }
                                        isSaveFirst = false;
                                    } else{
                                        dos.write(buffer, 0, frameLen);
                                    }
                                }
                            } catch (IOException e) {
                                e.printStackTrace();
                            }
                        } else {
                            // System.out.println(",");
                        }
                    }

                    if (inst.mscb != null) {
                        inst.mscb.notifyEvt(IMediaHelperCb.synplayrecord_end,
                                null);
                    }

                } catch (Exception e) {

                } finally {
                    if (audioRecord != null) {
                        try{
                            audioRecord.stop();
                            audioRecord.release();
                        } catch (IllegalStateException e) {
                            e.printStackTrace();
                        }
                    }
                    if (dos != null) {
                        try {
                            dos.flush();
                            dos.close();
                            Log.w("sync", "dos.close();");
                        } catch (IOException e) {
                            System.out.println(e.toString());
                            e.printStackTrace();
                        }
                    }
                    Log.w("sync", "stop record.");
                }
            }
        }).start();
    }

