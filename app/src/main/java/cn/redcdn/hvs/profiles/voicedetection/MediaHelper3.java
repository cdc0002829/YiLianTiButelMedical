    private long savetimetick = 0;
    private long synRecOriTick = 0;
    private long synRecBufTick = 0;
    private long synRecCount = 0;
    private int skipCount = 20;

    private void resetSaveRec() {
        synchronized (inst) {
            inst.savetimetick = 0;
        }
    }

    private void setSaveRec() {
        synchronized (inst) {
            inst.savetimetick = System.currentTimeMillis();
            System.out.println("set save rec..." + inst.savetimetick
                    + " synRecOriTick=" + synRecOriTick + " synRecCount="
                    + synRecCount + " synRecBufTick=" + synRecBufTick);
        }
    }

    private boolean isSaveRec() {
        synchronized (inst) {
            if (inst.savetimetick != 0) {
                // System.out.println("oritick:" + (synRecOriTick +
                // (synRecCount-skipCount) * 20) + " savetick:" + (savetimetick
                // + synRecBufTick) + "");
                if ((synRecOriTick + (synRecCount - skipCount) * 20) >= (savetimetick + synRecBufTick)) {
                    return true;
                }
            }
        }
        return false;
    }

    private int recLen() {
        int len = (int) (synRecOriTick + (synRecCount - skipCount) * 20 - (savetimetick + synRecBufTick));
        if (len < 20)
            return len;
        return 0;
    }

    private void initSyncRec() {
        synRecOriTick = 0;// System.currentTimeMillis();
        synRecBufTick = 0;
        synRecCount = 0;
    }

    private void recAFrame() {

        synRecCount++;
        if (synRecCount > skipCount) {
            if (synRecOriTick == 0)
                synRecOriTick = System.currentTimeMillis();
            long difTick = (synRecOriTick + (synRecCount - skipCount) * 20)
                    - System.currentTimeMillis();
            // System.out.println("" + difTick);
            if (difTick > synRecBufTick) {
                synRecBufTick = difTick;
                // System.out.println("synRecBufTick=" + synRecBufTick);
            }
        }
    }

